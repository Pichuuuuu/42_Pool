char	*ft_strupcase(char *str)
{
	int b;

	b = 0;
	while (str[b])
	{
		if (str[b] <= 'z' && str[b] >= 'a')
		{
			str[b] = str[b] - 32;
		}
		b++;
	}
	return (str);
}
