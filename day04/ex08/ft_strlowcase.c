char	*ft_strlowcase(char *str)
{
	int b;

	b = 0;
	while (str[b])
	{
		if (str[b] <= 'Z' && str[b] >= 'A')
		{
			str[b] = str[b] + 32;
		}
		b++;
	}
	return (str);
}
