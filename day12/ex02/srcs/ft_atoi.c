int		ft_atoi(char *str)
{
	int in;
	int c;

	in = 0;
	c = 0;
	while (*str == ' ' || *str == '\t' || *str == '\v'
		|| *str == '\f' || *str == '\r' || *str == '\n')
		str++;
	while (*str == '+' || *str == '-')
	{
		if (*str == '-')
			c++;
		str++;
	}
	while (*str <= '9' && *str >= '0')
	{
		in = in * 10;
		in = (in + *str - '0');
		str++;
	}
	c = c % 2;
	if (c)
		in = -1 * in;
	return (in);
}
